package org.elasticsearch.foreign.processor;

import org.elasticsearch.core.SuppressForbidden;

import java.lang.foreign.Arena;
import java.lang.foreign.MemorySegment;
import java.lang.foreign.ValueLayout;
import java.lang.reflect.Constructor;
import java.lang.reflect.Method;
import java.util.LinkedHashMap;
import java.util.Map;

/**
 * Behavioral tests for {@code @Critical} bindings generated by {@link ImplClassWriter}.
 * These tests instantiate the generated class and actually invoke the method against libc's {@code memcmp},
 * testing the downcall wiring end to end. {@code memcmp} is chosen as it requires no native library build dependency,
 * works on any platform, and it is pure/read-only.
 */
@SuppressForbidden(
    reason = "tests instantiate package-private processor-generated classes and invoke their public methods "
        + "cross-package; getDeclaredConstructor/setAccessible is the only way to do that via reflection"
)
public class CriticalGeneratedClassBehaviorTests extends ProcessorTestCase {

    private static final String MEMCMP_LIB_SENTINEL = """
        package test;
        import java.lang.foreign.MemorySegment;
        import org.elasticsearch.foreign.LibrarySpecification;
        import org.elasticsearch.foreign.Function;
        import org.elasticsearch.foreign.Critical;
        @LibrarySpecification
        public interface MemCmpLib {
            @Function("memcmp")
            @Critical(fallbackAdapter = Critical.UnsupportedFallback.class)
            int memcmp(MemorySegment a, MemorySegment b, long n);
        }
        """;

    /**
     * A {@code @Critical} binding with a real fallback adapter links and calls {@code memcmp} correctly.
     */
    public void testFallbackAdapterCriticalCall() throws Exception {
        Map<String, String> sources = new LinkedHashMap<>();
        sources.put("test.MemCmpAdapter", """
            package test;
            import java.lang.foreign.MemorySegment;
            import java.lang.invoke.MethodHandle;
            public final class MemCmpAdapter {
                public static int memcmp(MethodHandle mh, MemorySegment a, MemorySegment b, long n) throws Throwable {
                    return (int) mh.invokeExact(a, b, n);
                }
            }
            """);
        sources.put("test.MemCmpLib", """
            package test;
            import java.lang.foreign.MemorySegment;
            import org.elasticsearch.foreign.LibrarySpecification;
            import org.elasticsearch.foreign.Function;
            import org.elasticsearch.foreign.Critical;
            @LibrarySpecification
            public interface MemCmpLib {
                @Function("memcmp")
                @Critical(fallbackAdapter = MemCmpAdapter.class)
                int memcmp(MemorySegment a, MemorySegment b, long n);
            }
            """);

        CompilationResult result = compile(sources);
        assertTrue("Expected compilation to succeed but got errors: " + result.errors(), result.success());

        Bound bound = bindMemcmp(result);
        try (Arena arena = Arena.ofConfined()) {
            MemorySegment a = fill(arena.allocate(4));
            MemorySegment b = fill(arena.allocate(4));
            assertEquals("equal 4-byte segments compare equal", 0, (int) bound.method.invoke(bound.instance, a, b, 4L));
        }
    }

    /**
     * The {@code Critical.UnsupportedFallback} sentinel behaves differently per JDK, but these tests are always using
     * JDK >= 25, as they compile snippets of codes and run our processor (which requires JDK 24+), so this test asserts
     * the JDK 22+ behavior.
     * For JDK 22+ the binding is a normal critical call with heap segment support, so passing heap-backed {@link MemorySegment}s
     * links and calls {@code memcmp} correctly.
     */
    public void testUnsupportedFallbackSentinel() throws Exception {
        CompilationResult result = compile("test.MemCmpLib", MEMCMP_LIB_SENTINEL);
        assertTrue("Expected compilation to succeed but got errors: " + result.errors(), result.success());

        Bound bound = bindMemcmp(result);

        MemorySegment a = MemorySegment.ofArray(new byte[] { 0, 1, 2, 3 });
        MemorySegment b = MemorySegment.ofArray(new byte[] { 0, 1, 2, 3 });

        int res = (int) bound.method.invoke(bound.instance, a, b, 4L);
        assertEquals("equal 4-byte heap segments compare equal", 0, res);
    }

    private record Bound(Object instance, Method method) {}

    private static Bound bindMemcmp(CompilationResult result) throws Exception {
        Class<?> implClass = result.loadClass("test.MemCmpLib$Impl");
        Constructor<?> ctor = implClass.getDeclaredConstructor();
        ctor.setAccessible(true);
        Object instance = ctor.newInstance();
        Method memcmp = implClass.getMethod("memcmp", MemorySegment.class, MemorySegment.class, long.class);
        memcmp.setAccessible(true);
        return new Bound(instance, memcmp);
    }

    private static MemorySegment fill(MemorySegment segment) {
        for (int i = 0; i < segment.byteSize(); i++) {
            segment.set(ValueLayout.JAVA_BYTE, i, (byte) i);
        }
        return segment;
    }
}
